// Datatypes 
/*
2 datatypes in java
1. Primitive data type
2. Non- primitive data type

1. Primitive data type

byte - 8 bits
int - 4 bytes
short - 2 byte
long - 8 bytes

char - 2 bytes
boolean  - True or False value
float - 4 bytes - decimal values
double - 8 bytes
*/

package com.example;

public class Test2 {
		  public static void main(String[] args)
		 {
		    byte a = 90; // byte  -128 to 127
		    System.out.println("byte data type "+a);  
		     
		// Short 
		short s= 4000;
		System.out.println("short data type "+s);  

		// Int 
		int i =100000 ;
		System.out.println("int data type is "+i);
		
		//long- used for storing mobile numbers
		long l =987654321 ;
		System.out.println("long data type is "+l);
		
		//float
		float f = 3.1F;
		System.out.println("float data type is "+f);
		
		
		//Double
		double d = 19.99d;
		System.out.println("double data type is "+d);
		
		//boolean
		boolean b= true;
		System.out.println("Boolean data type is "+b);
		
		//char
		char c = 'a';
		System.out.println("Char data type is "+c);
		}

	}

